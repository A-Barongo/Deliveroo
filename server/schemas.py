from marshmallow import Schema, fields, validate, ValidationError

class ParcelSchema(Schema):
    id = fields.Integer(dump_only=True)
    description = fields.String(required=True, validate=validate.Length(min=1))
    status = fields.String(dump_only=True)
    sender_name = fields.String(required=True, validate=validate.Length(min=1))
    sender_phone_number = fields.Integer(required=True)
    pickup_location_text = fields.String(required=True, validate=validate.Length(min=1))
    destination_location_text = fields.String(required=True, validate=validate.Length(min=1))
    pick_up_longitude = fields.Float(required=True)
    pick_up_latitude = fields.Float(required=True)
    destination_longitude = fields.Float(required=True)
    destination_latitude = fields.Float(required=True)
    current_location_longitude = fields.Float(allow_none=True)
    current_location_latitude = fields.Float(allow_none=True)
    distance = fields.Float(allow_none=True)
    cost = fields.Float(dump_only=True)
    created_at = fields.DateTime(dump_only=True)
    updated_at = fields.DateTime(dump_only=True)
    recipient_name = fields.String(required=True, validate=validate.Length(min=1))
    recipient_phone_number = fields.Integer(required=True)
    courier_id = fields.Integer(allow_none=True)
    user_id = fields.Integer(dump_only=True)
    weight = fields.Float() 